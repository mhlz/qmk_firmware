void matrix_scan_user(void) {
    uint8_t layer = biton32(layer_state);

    switch (layer) {
        case BASE:
            set_led_off;
            break;
        case GAME:
        case SIMU:
            set_led_red;
            break;
        case PUNC:
        case NUMB:
            set_led_green;
            break;
        case CNFG:
            set_led_blue;
            break;
        default:
            break;
    }
};


